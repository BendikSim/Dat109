package no.hvl.dat109.eniteter;

/**
 * Klassen definerer en rute
 * @author Bendik Simonsen og Jon Vollset
 *
 */
public class Rute {
	 private int nr;
	 private int mapping;
	
	 
	 public Rute(int nr) {
		 this.nr = nr;
		 this.mapping = mapping;
	 }
	 public Rute(int nr, int mapping) {
		 this.nr = nr;
		 this.mapping = mapping;
	 }
	 
	 public int getNr() {
		return nr;
	}
	public int getMapping() {
		return mapping;
	}
	
	
	 
	 

}
